/**
 * reverse_array - a fun that reverses an array
 * @a: array to reverse
 * @n: number of elements to reverse
 * Return: destination string
 */
void reverse_array(int *a, int n)
{
int i = 0, j = 0;
int *b;

for (; a[i != n]; i++, j++)
b[j] = a[i];

i = 0;
j = j - 1;

for (; a[i != n]; i++, j--)
a[i] = b[j];

}
